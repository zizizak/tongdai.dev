    <h3 class="tieude">Tính năng kỹ thuật</h3>
    <p>- Tổng đài T64SIP do công ty TECAPRO (Việt Nam) sản xuất dùng cho mạng tổng đài quân sự cấp 3
        (cấp chiến thuật), vì vậy tổng đài được thiết kế gọn nhẹ, độ tin cậy cao,
        dễ lắp đặt và thay thế sửa chữa.</p>
    <p>- Tổng đài T64SIP có kết cấu vững chắc và khả năng hoạt động trong điều kiện cơ động; không cần điều hòa nhiệt độ
        khi làm việc.</p>
    <p>- Tổng đài T64SIP được xây dựng trên nền tảng công nghệ chuyển mạch TDM và chuyển mạch mềm IP nên có thể hoạt
        động
        như tổng đài chuyển tiếp giữa E1 và IP, hoặc tổng đài đầu cuối của mạng điện thoại (PSTN).</p>
    <p>- Tổng đài T64SIP có khả năng hoạt động với thuê bao analog và thuê bao IP (IP phone, Softphone).</p>
    <p>- Có khả năng giao tiếp với tổng đài khác qua trung kế E1 sử dụng báo hiệu R2-MFC hoặc trung kế IP sử dụng báo
        hiệu
        SIP.</p>
    <p>- Tự động nhận biết và hỗ trợ 2 loại thuê bao tương tự và thuê bao từ thạch (TA57B) trên cùng một cổng thuê bao.
    </p>
    <p>- Giao tiếp với tổng đài khác qua trung kế CO, trung kế luồng E1 hay trung kế ĐKX vô tuyến.</p>
    <p>- Tổng đài có khả năng phân cấp thuê bao để hạn chế các cuộc gọi theo từng mức ưu tiên khác nhau.</p>
    <p>- Người khai thác tổng đài có thể sử dụng các máy điện thoại thông thường để thay đổi và kiểm tra các thông số
        cấu
        hình của tổng đài thông qua thao tác quay các mã dịch vụ hệ thống tương ứng.</p>
    <p>- Tổng đài hỗ trợ lưu tối đa 3 cấu hình hoạt động khác nhau để người sử dụng có thể khôi phục lại cấu hình đã
        khai
        báo mà không cần phải khai báo lại các thông số của hệ thống.</p>
    <p>- Có khả năng truyền dữ liệu IP tốc độ cao bằng cách ghép các kênh thoại (có thể tùy biến số kênh cần ghép để
        tăng
        tốc độ và băng thông bằng bảng mạch IPX8), mỗi kênh có tốc độ khoảng 30 – 50Kbps.</p>
    <p>- Có khả năng đánh số theo kế hoạch đánh số của Quân đội và các doanh nghiệp viễn thông.</p>
    <p>- Có khả năng phân cấp thuê bao với các mức ưu tiên khác nhau.</p>
