    <p><strong>Bước 3: </strong>Tiến hành vệ sinh, hút bụi bề mặt bảng mạch CPU, luồng E1, Đáy tổng đài (chú ý không tháo các bảng mạch này ra khỏi khung tổng đài khi bảo dưỡng).</p>
    <p><strong>Bước 4: </strong>Tháo bảng mạch nguồn dự phòng ra khỏi khung tổng đài rồi tiến hành vệ sinh, hút bụi bề mặt bảng mạch. Kiểm tra ngoại dạng các vật tư linh kiện xem có bị quá nhiệt (cháy, xám, thay đổi màu sắc,biến dạng, bong tróc mạch in). Nếu bị các lỗi trên phải tiến hành sửa chữa và khắc phục theo hướng dẫn rồi mới được phép lắp lại.</p>
    <p><strong>Bước 5: </strong>Tháo bảng mạch thuê bao và trung kế ra khỏi khung tổng đài rồi tiến hành vệ sinh, hút bụi bề mặt bảng mạch. Kiểm tra ngoại dạng các vật tư linh kiện xem có bị cháy, xám, thay đổi màu sắc, biến dạng, bong tróc mạch in ... Nếu bị các lỗi trên phải tiến hành sửa chữa và khắc phục theo hướng dẫn.</p>
    <p><strong>Bước 6: </strong>Tiến hành lắp các bảng mạch theo thứ tự : bảng mạch nguồn, bảng mạch thuê bao, bảng mạch trung kế CO/ĐKX (chú ý trước khi lắp vào phải kiểm tra kỹ ngoại dạng một lần nữa để đảm bảo không bị chạm chập, dính bụi bẩn và các vật dẫn điện khác khi bảo dưỡng).</p>
    <p><strong>Bước 7: </strong>Tiến hành kiểm tra lại tính năng, thông số kỹ thuật của từng thuê bao, trung kế CO/ĐKX, từng luồng E1.</p>
