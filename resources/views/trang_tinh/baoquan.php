    <h5>1. Khái niệm</h5>
    <p>Bảo quản, bảo dưỡng kỹ thuật là toàn bộ các công việc và biện pháp tổ chức, biện pháp kỹ thuật được tiến hành
        nhằm duy trì tính năng kỹ chiến thuật của trang bị, thiết bị kỹ thuật nhằm duy trì độ tin cậy và phòng ngừa hư
        hỏng, được tiến hành ở mọi trạng thái sử dụng, như: sẵn sàng chiến đấu, khai thác thường xuyên và niêm cất…
    </p>
    <p>Các nội dung chính của bảo quản, bảo dưỡng kỹ thuật gồm: kiểm tra, làm sạch, siết chặt các mối lắp ghép, điều
        chỉnh các tham số kỹ thuật; bổ sung hoặc thay thế dầu mỡ bôi trơn; khắc phục hoặc thay thế các chi tiết phụ tùng
        bị hư hỏng hoặc hết độ tin cậy.</p>
    <h5>2. Quy định chung</h5>
    <p>Trạm tổng đài là một thành phần quan trọng trong hệ thống thông tin liên lạc thường xuyên và sẵn sàng chiến đấu
        nên công tác bảo quản, bảo dưỡng phải được thực hiện bởi các tổ BĐKT cơ động đã được tập huấn thực hiện. Lực
        lượng khai thác tại chỗ tham gia dưới sự phân công và hướng dẫn của tổ kỹ thuật.</p>
    <p>Trước khi bảo quản, bảo dưỡng kiểm tra hệ thống sổ sách: hồ sơ hoàn công, lý lịch, theo dõi sửa chữa (nắm bắt sơ
        đồ lắp đặt, sơ đồ đấu nối, những xử lý trong sửa chữa, khắc phục sự cố…) và tình trạng kỹ thuật hiện tại để đưa
        ra nội dung bảo quản, bảo dưỡng hợp lý. Phân công, giao nhiệm vụ cụ thể cho từng cá nhân, từng bộ phận có phân
        công người quản lý phụ trách toàn bộ quá trình bảo quản, bảo dưỡng.</p>
    <p>Trạm tổng đài là một thành phần trong hệ thống thông tin quân sự, bảo đảm thông tin liên lạc cho nhiệm vụ thường
        xuyên và SSCĐ. Vì vậy, trước khi thực hiện quá trình bảo quản, bảo dưỡng phải xây dựng kế hoạch và được cấp có
        thẩm quyền phê duyệt. Khi kế hoạch đã được phê duyệt, đó sẽ là căn cứ pháp lý để tiến hành công tác bảo quản,
        bảo dưỡng. Báo cáo trực Chỉ huy đơn vị, Trung tâm điều hành và Trực ban tác chiến Binh chủng TTLL (Z84) để có sự
        thống nhất đồng bộ trong việc vu hồi thông tin, không làm mất liên lạc với những nhiệm vụ quan trọng và đột
        xuất. Chỉ được tiến hành bảo quản, bảo dưỡng khi có đầy đủ các căn cứ pháp lý trên.</p>
    <p>Yêu cầu trang bị, vật tư, nhiên liệu, dụng cụ phục vụ bảo quản, bảo dưỡng phải bảo đảm đủ số lượng, chất lượng;
        sử dụng các phương tiện đo đã được kiểm chuẩn. Trước bảo dưỡng yêu cầu huấn luyện bổ sung về an toàn lao động,
        tính năng kỹ thuật và quy trình bảo dưỡng cho lực lượng thực hiện.</p>
    <p>Thực hiện bảo quản, bảo dưỡng phải tuân thủ tất cả các quy tắc an toàn trong lao động; quy tắc an toàn trong
        hướng dẫn sử dụng thiết bị của nhà sản xuất; nguyên tắc an toàn khi sử dụng điện, khi thao tác bảo quản, bảo
        dưỡng và sửa chữa điện tử, khi thao tác cơ khí, khi tiếp xúc hóa chất, khi tháo lắp thiết bị, khi sử dụng trang
        thiết bị, dụng cụ. Nếu xảy ra sự cố phải báo cáo ngay Trực Chỉ huy đơn vị và Trực ban tác chiến để kịp thời xử
        lý.</p>
    <p>Khi bảo quản, bảo dưỡng xong, đưa trạm vào hoạt động bình thường phải báo cáo sơ bộ tình trạng kỹ thuật về Trực
        chỉ huy và Trực ban tác chiến các cấp và các đơn vị có liên quan, phối hợp xử lý những tồn tại (nếu có). Khi
        chắc chắn tình trạng kỹ thuật của trạm đã tốt, ghi chép sổ sách, bàn giao cho đơn vị đầy đủ mới rời khỏi vị trí.
    </p>
    <h5>3. Nội dung bảo quản ngày</h5>
    <h5>a) Mục đích</h5>
    <p>- Bảo đảm tổng đài, phòng máy luôn sạch sẽ, đồng bộ</p>
    <p>- Phòng ngừa hư hỏng cho tổng đài , bảo đảm tốt thông tin liên lạc.</p>
    <h5>b) Yêu cầu</h5>
    <p>- Chấp hành nghiêm các quy định về bảo quản thiết bị.</p>
    <p>- Không được làm mất hoặc có nguy cơ làm mất thông tin liên lạc.</p>
    <p>- Sau bảo quản ngày, bảo đảm môi trường tốt nhất cho thiết bị hoạt động.</p>
    <h5>c) Nội dung và các bước thực hiện</h5>
    <p>Bảo quản ngày được thực hiện vào cuối ngày làm việc hoặc sau mỗi phiên làm việc, sau mỗi ca trực. Đối với tổng
        đài lắp cố định trên xe hoặc sử dụng cơ động, nội dung này được thực hiện sau mỗi phiên làm việc. </p>
    <p>Nội dung bảo quản gồm:</p>
    <p> - Kiểm tra, lau chùi, làm sạch thiết bị.</p>
    <p> - Kiểm tra báo cảnh, tình trạng hoạt động của thiết bị.</p>
    <p> - Kiểm tra đồng bộ của thiết bị</p>
    <p> - Vệ sinh sạch các phòng máy, phòng trực</p>
    <p> - Ghi chép sổ sách bàn giao phiên (ca).</p>
    <p>Các bước thực hiện:</p>
    <p><strong>Bước 1: </strong>Kiểm tra bằng mắt thường cả phía trước và phía sau thiết bị, nếu thấy không có bụi bẩn,
        các đầu dây nối
        tốt, cơ khí chắc chắn, thiết bị hoạt động bình thường thì chuyển sang Bước 6. Nếu thấy bụi bẩn, ốc vít, đầu nối
        bị hở, cáp nguồn, cáp đất không chắc chắn. Chuyển sang Bước 2.</p>
    <p><strong>Bước 2: </strong>Dùng giẻ lau, lau sạch các vết bẩn trên mặt máy. </p>
    <p><strong>Bước 3: </strong>Sử dụng chổi lông phủi sạch bụi trên mặt máy. </p>
    <p><strong>Bước 4: </strong>Kiểm tra bằng mắt thường các đèn LED trên bảng mạch nguồn, thuê bao, luồng E1, trung kế
        CO, DKX phải ở trạng thái sang bình thường . </p>
    <p><strong>Bước 5: </strong>Quan sát bằng mắt dây đấu nguồn, thuê bao, luồng E1 nếu thấy bất thường như bị đứt, gãy,
        hoặc bị bung thì tiến hành báo chỉ huy để xử lý. </p>
    <p><strong>Bước 6: </strong>Ghi chép sổ sách theo dõi, kết thúc bảo quản ngày. </p>
    <h5>4. Nội dung bảo quản tuần</h5>
    <h5>a) Mục đích</h5>
    <p>- Bảo đảm các thuê bao, trung kế CO, trung kế điều khiển xa, trung kế luồng E1 hoạt động tốt, đúng theo chỉ tiêu
        kỹ thuật của nhà sản xuất.</p>
    <p>- Phòng ngừa hư hỏng cho tổng đài, phát hiện và báo cáo kịp thời các sự cố phát sinh, bảo đảm tốt thông tin liên
        lạc.</p>
    <h5>b) Yêu cầu</h5>
    <p>- Chấp hành nghiêm các quy định về bảo quản thiết bị.</p>
    <p>- Không được làm mất hoặc có nguy cơ làm mất thông tin liên lạc.</p>
    <h5>c) Nội dung và các bước thực hiện</h5>
    <p>Bảo quản tuần được thực hiện vào chiều ngày thứ 6 hàng tuần. Nội dung thực hiện giống như bảo quản ngày và thêm
        các nội dung sau:</p>
    <p>- Kiểm tra lại toàn bộ hoạt động của các thuê bao, trung kế, truyền số liệu.</p>
    <p>- Huấn luyện bổ sung về khai thác, rút kinh nghiệm và huấn luyện về xử lý sự cố kỹ thuật trong tuần (theo kế
        hoạch của trạm).</p>
    <p>- Ghi chép sổ sách, tổng hợp đánh giá, báo cáo theo quy định.</p>
    <p><strong>Lưu ý:</strong> Bảo quản trạm máy xong phải bảo quản các dụng cụ, vật tư phục vụ bảo quản để tái sử dụng
        vào lần sau.</p>



